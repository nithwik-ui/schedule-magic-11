import { useState, useEffect, createContext, useContext } from "react";
import { supabase } from "@/integrations/supabase/client";
import type { User, Session } from "@supabase/supabase-js";

interface Profile {
  id: string;
  user_id: string;
  full_name: string | null;
  degree: string | null;
  year: string | null;
  batch: string | null;
}

  interface AuthContextType {
  user: User | null;
  session: Session | null;
  profile: Profile | null;
  loading: boolean;
  signUp: (email: string, password: string, fullName: string) => Promise<{ error: any }>;
  signIn: (email: string, password: string) => Promise<{ error: any }>;
  signOut: () => Promise<void>;
  // updateProfile returns a source field to indicate where it was saved ('remote' | 'local')
  updateProfile: (data: Partial<Profile>) => Promise<{ error: any; source?: "remote" | "local" }>;
  refreshProfile: () => Promise<void>;
}

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export const AuthProvider = ({ children }: { children: React.ReactNode }) => {
  const [user, setUser] = useState<User | null>(null);
  const [session, setSession] = useState<Session | null>(null);
  const [profile, setProfile] = useState<Profile | null>(null);
  const [loading, setLoading] = useState(true);

  const fetchProfile = async (userId: string) => {
    const { data } = await supabase
      .from("profiles")
      .select("*")
      .eq("user_id", userId)
      .single();
    setProfile(data);
  };

  const LOCAL_KEY = "sru_profile";

  const loadLocalProfile = (): Profile | null => {
    try {
      const raw = localStorage.getItem(LOCAL_KEY);
      if (!raw) return null;
      return JSON.parse(raw) as Profile;
    } catch (e) {
      console.error("Failed to parse local profile:", e);
      return null;
    }
  };

  const refreshProfile = async () => {
    if (user) await fetchProfile(user.id);
  };

  useEffect(() => {
    const { data: { subscription } } = supabase.auth.onAuthStateChange(
      async (_event, session) => {
        setSession(session);
        setUser(session?.user ?? null);
        if (session?.user) {
          setTimeout(() => fetchProfile(session.user.id), 0);
        } else {
          setProfile(null);
        }
        setLoading(false);
      }
    );

    supabase.auth.getSession().then(({ data: { session } }) => {
      setSession(session);
      setUser(session?.user ?? null);
      if (session?.user) fetchProfile(session.user.id);
      setLoading(false);
    });

    return () => subscription.unsubscribe();
  }, []);

  // Load local profile immediately on mount (so preferences persist across refresh)
  useEffect(() => {
    try {
      const local = loadLocalProfile();
      if (local) setProfile(local);
    } catch (e) {
      // ignore
    }
  }, []);

  const signUp = async (email: string, password: string, fullName: string) => {
    const { error } = await supabase.auth.signUp({
      email,
      password,
      options: {
        data: { full_name: fullName },
        emailRedirectTo: window.location.origin,
      },
    });
    return { error };
  };

  const signIn = async (email: string, password: string) => {
    const { error } = await supabase.auth.signInWithPassword({ email, password });
    return { error };
  };

  const signOut = async () => {
    await supabase.auth.signOut();
    setProfile(null);
  };

  const updateProfile = async (data: Partial<Profile>) => {
    if (user) {
      const { error } = await supabase
        .from("profiles")
        .update(data)
        .eq("user_id", user.id);
      if (!error) await fetchProfile(user.id);
      return { error, source: "remote" as const };
    }

    // Fallback to localStorage for unauthenticated users
    try {
      const existing = loadLocalProfile();
      const merged = { ...(existing || {}), ...data } as Profile;
      // Ensure minimal fields for our local profile object
      if (!merged.id) merged.id = "local";
      if (!merged.user_id) merged.user_id = "local";
      localStorage.setItem(LOCAL_KEY, JSON.stringify(merged));
      setProfile(merged);
      return { error: null, source: "local" as const };
    } catch (e) {
      console.error("Failed to save local profile", e);
      return { error: e };
    }
  };

  return (
    <AuthContext.Provider
      value={{ user, session, profile, loading, signUp, signIn, signOut, updateProfile, refreshProfile }}
    >
      {children}
    </AuthContext.Provider>
  );
};

export const useAuth = () => {
  const context = useContext(AuthContext);
  if (!context) throw new Error("useAuth must be used within AuthProvider");
  return context;
};
